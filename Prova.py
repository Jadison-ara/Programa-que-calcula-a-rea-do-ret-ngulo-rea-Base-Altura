# programa deve calcular a área usando a fórmula: Área=Base×Altura

# variaveis que solicitam o  valor da base e da altura

altura = int (input("Digite o valor da altura do retângulo :"))
base = int (input("Digite o valor da base do retângulo :"))

#Variavel que  calcula a área do retângulo

area = altura * base

# saida do programa que exibe o valor da área do retângulo

print (f"Altura:{altura} x Base:{base}  = Área: {area}")


